# > CONVERSÃO DE TIPOS

idade = '18'

print(idade, type(idade))

idade_inteira = int(idade)

print(idade_inteira, type(idade_inteira))

""" 
Tipos de variáveis
1. int: números inteiros (... -2, -1, 0 , 1, 2...)
2. float: números reais (... -0.1, 0, 0.1, 1, 2, 2.5 ...)
3. str [string]: cadeia de caracteres (dados textuais em "")
4. bool: valores lógicos [booleanos] (True, False)
"""
altura = float( input('Informe sua altura: ') )

print(altura, type(altura))