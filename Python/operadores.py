# > Operações Matemáticas
"""
Soma: +
Subtração: -
Multiplicação: *
Divisão: / 
Divisão Inteira: //
Resto da Divisão: %
Potência: **
"""

numero1 = 20
numero2 = 10
numero3 = 3

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(20 % numero3)
print(2 ** numero3)

